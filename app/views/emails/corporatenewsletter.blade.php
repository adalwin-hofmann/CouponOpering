                          Good Morning Team Save!
                          <br>
                          <br>
                          Wow – another quarter has passed and what a significant one for us here at SAVE. As you all know, we have accomplished our July 1st goal of transitioning our old system over to Magazine Manager and boy was it a project!  It took just over 10 months for all of us to do our part to now be operating under a brand new, fully electronic workflow system.  Our previous system was good for us but it was imperative to take a leap to a new program that will allow us to scale, work more efficiently and leverage more productivity than ever before. I think I am most excited about the reports this tool will create – dashboards to help us make real time decisions and make sure we are all on the same page. A lot more to come on this subject!
                          <br>
                          <br>
                          Mike and I also spent the last quarter meeting with every team and every department in the company to better understand what has been going on, what is needed to move forward and just what we need to make this next fiscal year better than the last. The number one thing we learned was it is very important to listen…listen to all of you because it is you that makes this company tick. By listening to all of you we have gathered our roadmap for next year and would like to share it with you all...
                          <br>
                          <br>
                          Here are the 2015/2016 initiatives we will focus on this year thanks to all of you...
                          <br>
                          <ul>
                            <li>Sales Training (new &amp; refresher training) / Sales Manager's Training</li>
                            <li>Hiring Sales Talent</li>
                            <li>Corporate Support for Sales</li>
                            <li>Product Look / Marketing</li>
                            <li>Sales Incentives</li>
                            <li>Digital Sales Training</li>
                          </ul>

                          Our focus is on &quot;SALES&quot; – how we can create sales, add sales, help sales be more effective, train sales, support sales to make the job easier/simplified. We are a SALES ORGANIZATION and we want to create a culture that breeds sales.
                          <br>
                          <br>
                          On behalf of the administration staff (Production, Digital, Accounting, Admin Support) we will be working hard to train ourselves and these departments to better leverage our sales efforts. We have a phenomenal team and it takes all of us to make the sales process effective.
                          <br>
                          <br>
                          In recognition of everyone's help in reaching the July 1st goal of transitioning to Magazine Manager I would like to offer an EMPLOYEE APPRECIATION DAY in all markets/departments. I will be providing lunch for everyone, offering some great prize packages and hosting a challenge where everyone can engage with and share with one another how very important you are to SAVE. More to come on this from Jillian.
                          <br>
                          <br>
                          Have an amazing summer everyone!

