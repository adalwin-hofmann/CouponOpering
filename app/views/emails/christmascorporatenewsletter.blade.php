                        Merry Christmas, Happy Hanukah, Feliz Navidad and HAPPY HOLIDAYS!
                        <br>
                        <br>
                        Whatever kind of festivities you are celebrating during this time of year - my hope is that you enjoy them. So many times we rush through life, through events, through the day and we do not realize what we are missing that is right in front of us. Life is precious and it should be lived intentionally!  I can’t imagine getting up every day and not caring what the day has in store or not to be grateful for what I have or not to live it positively. What I can tell you is that it is up to YOU. Your life is based on all the decisions YOU make.This is the perfect time of year to sit down and list out your personal and professional goals for the new year - what will YOU accomplish?
                        <br>
                        <br>
                        I share this with you because this is going to be part of our motto for 2105 as an organization. Our goal will be to take personal responsibility, focus on the solutions, grow, work together and reach success. We have a ton of growing still as an organization - to get there we need support. My commitment to all of you is resources to get us there.
                        <br>
                        <br>
                        Starting right after the new year the management team will be reading a development piece called "Adults at Work". This book helps organizations just like us move past the blame game and work together as a team and taking on personal responsibilities. This will not only help in our professional lives but also our personal lives. We will have a workshop on this material in March. All employees will take part in mini workshops leading up to the one in March.
                        <br>
                        <br>
                        I have always been told we should never stop learning - this is an indicator that we have settled right where we are. So, knowing this - we could all be better, we could all use development and learn more so, starting in 2015 we will be offering development courses for all on a quarterly basis.
                        <br>
                        <br>
                        My hope is that we grow as a company "together", we learn from one another, we are encouraged by one another. Bottom line, we are all here for the same goal as it takes a team to accomplish it. Let’s have some fun doing it. Speaking of fun...check out all the pictures of our awards recipients this year that were presented at our holiday parties in all markets. Congrats again everyone!
                        <br>
                        <br>
                        Have a warm and wonderful holiday season and I wish you and your families a blessed 2015 - remember, it’s up to YOU!
 
